#include "libft.h"

int	ft_atoi(const char *str)
{
	long	i;
	long	neg;
	long	num;

	i = 0;
	neg = 0;
	num = 0;
	while (str[i] != '\0' && (str[i] == 32 || str[i] == '\t' || str[i] == '\n'
			|| str[i] == '\r' || str[i] == '\v' || str[i] == '\f'))
		i++;
	if (str[i] != '\0' && str[i] == '-')
	{
		neg = 1;
		i++;
	}
	else if (str[i] == '+')
		i++;
	while (str[i] != '\0' && ft_isdigit(str[i]))
		num = (num * 10) + (str[i++] - '0');
	if (neg == 1)
		return (-num);
	return (num);
}
